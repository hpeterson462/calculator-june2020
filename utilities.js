export function add(value1, value2) {
  return Number(value1) + Number(value2);
}

export function sub(value1, value2) {
  return Number(value1) - Number(value2);
}

export function mul(value1, value2) {
  return Number(value1) * Number(value2);
}

export function div(value1, value2) {
  return Number(value1) / Number(value2);
}
